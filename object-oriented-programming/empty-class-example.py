''''
This is an example for CREATING EMPTY CLASS AND LATER WE DEFINE THE VARIBLES
'''

class emptyClass():
    pass


objectCreated=emptyClass()
objectCreated.name='lee'
objectCreated.country='chile'
objectCreated.zipcode=8372489

print('Name :', objectCreated.name, 'Country :', objectCreated.country, 'Zipcode :',objectCreated.zipcode)
