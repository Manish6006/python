'''
This is an example for CREATE COPY OF EXISITING SET data structure
'''
superHeroSet=set(['ironman','spiderman','superman','batman','wonder women','caption america'])

#copying the original set data structure
copySuperHeroSet=superHeroSet[:]

#printing original set
print('Original set before removing an item: ', superHeroSet)

#printing copy set
print('Copied set before removing an item: ',copySuperHeroSet)
