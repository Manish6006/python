'''
This is an example for DISPLAY INDEX WISE ITEM FROM SEQUENCE
'''

countryName=['India','America','Singapore','Hong Kong','Chile','Peru']

# Indexing
print('Item[3] :', countryName[3])

#print the first item from the sequence
print('Item[0] :', countryName[0])

#print the last item from the sequence
print('Item[last] :', countryName[len(countryName)-1]) 
